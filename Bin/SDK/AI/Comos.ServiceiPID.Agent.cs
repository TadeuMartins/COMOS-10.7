	private static string JsonStr(string json, string field)
	{
		Match match = Regex.Match(json, "\"" + field + "\"\\s*:\\s*\"((?:[^\"\\\\]|\\\\.)*)\"");
		return match.Success ? match.Groups[1].Value.Replace("\\\"", "\"").Replace("\\\\", "\\") : null;
	}

	private static double JsonNum(string json, string field)
	{
		Match match = Regex.Match(json, "\"" + field + "\"\\s*:\\s*([\\d.\\-]+)");
		double result;
		return (match.Success && double.TryParse(match.Groups[1].Value, NumberStyles.Float, CultureInfo.InvariantCulture, out result)) ? result : 0.0;
	}

