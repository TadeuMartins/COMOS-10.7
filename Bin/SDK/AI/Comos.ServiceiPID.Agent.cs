		{
			text = text.Substring(1);
		}
		if (text.StartsWith("xl/", StringComparison.OrdinalIgnoreCase))
		{
			return text;
		}
		if (text.StartsWith("worksheets/", StringComparison.OrdinalIgnoreCase))
		{
			return "xl/" + text;
		}
		return "xl/" + text;
	}

