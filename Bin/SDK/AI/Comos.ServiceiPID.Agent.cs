	}

	private object DrawTemplateObject(object targetDocObj, IComosDProject currentProject, int documentType, string tag, string description, string systemFullName, double x, double y, string documentUID)
	{
		// NATIVE APPROACH: Use IXDocDeveloper.CreateTemplate — the programmatic
		// equivalent of dragging a template document from the navigator and
		// dropping it onto the target diagram. This replicates ALL objects,
		// connections, and coordinates exactly as the template defines them.
		StringBuilder diag = new StringBuilder();
		try
		{
			// 1. Resolve template document
			object templateObj = ResolveTemplateDocument(currentProject, systemFullName, description, diag);
			if (templateObj == null)
			{
				return new
				{
					success = false,
					error = "Template document not resolved. Use a valid template path/name from COMOS templates.",
					route = "template",
					diag = diag.ToString(),
					hint = "Ensure the template document exists in the project's template tree."
				};
			}
			IComosDDocument templateDoc = templateObj as IComosDDocument;
			if (templateDoc == null)
			{
				try { templateDoc = (IComosDDocument)(dynamic)templateObj; } catch { }
			}
			string templateName = "";
			try { templateName = templateDoc != null ? (templateDoc.Name ?? "") : ""; } catch { }
			diag.AppendLine("Template doc resolved, Name=" + templateName);
			if (templateDoc == null)
			{
				return new
				{
					success = false,
					error = "Resolved object is not an IComosDDocument.",
					route = "template",
					diag = diag.ToString()
				};
			}

			// 2. Get the target document and open its Report
			IComosDDocument targetDoc = targetDocObj as IComosDDocument;
			if (targetDoc == null)
			{
				try { targetDoc = (IComosDDocument)(dynamic)targetDocObj; } catch { }
			}
			if (targetDoc == null)
			{
				return new
				{
					success = false,
					error = "Target document could not be cast to IComosDDocument.",
					route = "template",
					diag = diag.ToString()
				};
			}

			dynamic report = targetDoc.Report();
			if (report == null)
			{
				return new
				{
					success = false,
					error = "Target document has no Report object.",
					route = "template",
					diag = diag.ToString()
				};
			}
			report.Open();
			diag.AppendLine("Target report opened");

			// 3. Get ReportDocument and cast to IXDocDeveloper
			dynamic reportDocument = report.ReportDocument;
			if (reportDocument == null)
			{
				return new
				{
					success = false,
					error = "ReportDocument is null after Report.Open().",
					route = "template",
					diag = diag.ToString()
				};
			}
			diag.AppendLine("ReportDocument obtained");

			// 4a. Record the template's original owner/parent BEFORE CreateTemplate
			//     Some diagram types (e.g. multifilar) MOVE the template instead of
			//     copying it. We detect this and restore it afterward.
			object originalOwner = null;
			string originalOwnerSfn = "";
			try
			{
				originalOwner = ((dynamic)templateDoc).owner;
				if (originalOwner != null)
				{
					try { originalOwnerSfn = ((dynamic)originalOwner).SystemFullName(); }
					catch { originalOwnerSfn = "(could not read SFN)"; }
				}
				diag.AppendLine("4a: Original owner SFN=" + originalOwnerSfn);
			}
			catch (Exception exOwn)
			{
				diag.AppendLine("4a: Could not read original owner: " + exOwn.Message);
			}

			// 4b. Call CreateTemplate — native drag-drop equivalent
			//     IXDocDeveloper.CreateTemplate(IComosBaseObject BaseObj, double x, double y)
			//     This instantiates all devices, connections, and coordinates from the
			//     template document onto the target diagram at position (x, y).
			string createMethod = "";
			IXDocDeveloper xDocDev = reportDocument as IXDocDeveloper;
			if (xDocDev == null)
			{
				try { xDocDev = (IXDocDeveloper)reportDocument; }
				catch { diag.AppendLine("Direct cast to IXDocDeveloper failed, trying dynamic"); }
			}

			if (xDocDev != null)
			{
				// Prefer CreateTemplateInBackground (no UI refresh overhead)
				try
				{
					xDocDev.CreateTemplateInBackground((IComosBaseObject)templateDoc, x, y, 0.0);
					createMethod = "IXDocDeveloper.CreateTemplateInBackground OK";
					diag.AppendLine(createMethod);
				}
				catch (Exception ex1)
				{
					diag.AppendLine("CreateTemplateInBackground ERR: " + ex1.Message);
					// Fallback to CreateTemplate (with UI)
					try
					{
						xDocDev.CreateTemplate((IComosBaseObject)templateDoc, x, y);
						createMethod = "IXDocDeveloper.CreateTemplate OK";
						diag.AppendLine(createMethod);
					}
					catch (Exception ex2)
					{
						diag.AppendLine("CreateTemplate ERR: " + ex2.Message);
						// Final fallback: CreateTemplateWithRotation
						try
						{
							xDocDev.CreateTemplateWithRotation((IComosBaseObject)templateDoc, x, y, 0.0);
							createMethod = "IXDocDeveloper.CreateTemplateWithRotation OK";
							diag.AppendLine(createMethod);
						}
						catch (Exception ex3)
						{
							diag.AppendLine("CreateTemplateWithRotation ERR: " + ex3.Message);
						}
					}
				}
			}

			// Dynamic fallback if typed IXDocDeveloper cast failed
			if (string.IsNullOrEmpty(createMethod))
			{
				try
				{
					reportDocument.CreateTemplateInBackground((object)templateDoc, x, y, 0.0);
					createMethod = "dynamic.CreateTemplateInBackground OK";
					diag.AppendLine(createMethod);
				}
				catch (Exception exd1)
				{
					diag.AppendLine("dynamic.CreateTemplateInBackground ERR: " + exd1.Message);
					try
					{
						reportDocument.CreateTemplate((object)templateDoc, x, y);
						createMethod = "dynamic.CreateTemplate OK";
						diag.AppendLine(createMethod);
					}
					catch (Exception exd2)
					{
						diag.AppendLine("dynamic.CreateTemplate ERR: " + exd2.Message);
					}
				}
			}

			if (string.IsNullOrEmpty(createMethod))
			{
				return new
				{
					success = false,
					error = "All CreateTemplate methods failed. See diag for details.",
					route = "template",
					tag = tag,
					templateName = templateName,
					diag = diag.ToString()
				};
			}

			// 4c. Detect if template was MOVED (owner changed) and restore it
			//     For multifilar diagrams, CreateTemplate may move the template
			//     document under the target diagram instead of copying it.
			//     If that happened, we move it back to its original parent.
			if (originalOwner != null)
			{
				try
				{
					object currentOwner = ((dynamic)templateDoc).owner;
					string currentOwnerSfn = "";
					try { currentOwnerSfn = ((dynamic)currentOwner).SystemFullName(); }
					catch { currentOwnerSfn = "(unknown)"; }

					bool wasMoved = false;
					if (currentOwner == null && originalOwner != null)
					{
						wasMoved = true;
					}
					else if (currentOwner != null && originalOwner != null)
					{
						// Compare by SystemFullName — if different, template was relocated
						try
						{
							wasMoved = !string.Equals(originalOwnerSfn, currentOwnerSfn, StringComparison.OrdinalIgnoreCase);
						}
						catch
						{
							// If comparison fails, compare COM object identity
							wasMoved = !object.ReferenceEquals(originalOwner, currentOwner);
						}
					}

					if (wasMoved)
					{
						diag.AppendLine("4c: Template MOVED detected! Original=" + originalOwnerSfn + " Current=" + currentOwnerSfn);
						diag.AppendLine("4c: Restoring template to original parent...");

						// Strategy: Use CopyAll/Paste to copy template back, then delete the moved copy
						// OR: Use ChangeOwner if available
						bool restored = false;

						// Try ChangeOwner first (cleanest approach)
						try
						{
							((dynamic)templateDoc).ChangeOwner((object)originalOwner);
							restored = true;
							diag.AppendLine("4c: ChangeOwner OK — template restored to " + originalOwnerSfn);
						}
						catch (Exception exCo)
						{
							diag.AppendLine("4c: ChangeOwner ERR: " + exCo.Message);
						}

						// Fallback: Move via CopyAll + Paste into original owner, then delete from current location
						if (!restored)
						{
							try
							{
								dynamic clipObj = ((dynamic)templateDoc).CopyAll();
								if (clipObj != null)
								{
									object pastedRef = null;
									int pasteRc = (int)((dynamic)originalOwner).Paste(clipObj, ref pastedRef, true);
									diag.AppendLine("4c: CopyAll+Paste rc=" + pasteRc + " pastedRef=" + (pastedRef != null ? "OK" : "null"));
									if (pasteRc == 0 || pastedRef != null)
									{
										// Delete the moved (current) copy from the target diagram
										try
										{
											((dynamic)templateDoc).Delete();
											diag.AppendLine("4c: Deleted moved copy from target diagram");
										}
										catch (Exception exDel)
										{
											diag.AppendLine("4c: Delete moved copy ERR (non-critical): " + exDel.Message);
										}
										restored = true;
									}
								}
							}
							catch (Exception exPaste)
							{
								diag.AppendLine("4c: CopyAll+Paste fallback ERR: " + exPaste.Message);
							}
						}

						if (!restored)
						{
							diag.AppendLine("4c: WARNING — could not restore template to original location. Template may have been moved under the target diagram.");
						}
					}
					else
					{
						diag.AppendLine("4c: Template owner unchanged — no move detected (OK).");
					}
				}
				catch (Exception exCheck)
				{
					diag.AppendLine("4c: Owner check ERR: " + exCheck.Message);
				}
			}

			// 5. Save the report and commit
			try { report.Save(); diag.AppendLine("Report.Save OK"); }
			catch (Exception exs) { diag.AppendLine("Report.Save ERR: " + exs.Message); }
			try { Workset.SaveAll(); } catch { }

			return new
			{
				success = true,
				route = "template",
				tag = tag,
				description = description,
				templateSourceSfn = systemFullName,
				templateName = templateName,
				x = x,
				y = y,
				documentUID = documentUID,
				createMethod = createMethod,
				message = string.Format("Template '{0}' instantiated at ({1},{2}) via {3}. All devices, connections, and coordinates replicated from template.",
					templateName, x, y, createMethod),
				diag = diag.ToString()
			};
		}
		catch (Exception ex)
		{
			return new
			{
				success = false,
				route = "template",
				tag = tag,
				error = "DrawTemplateObject error: " + ex.Message,
				diag = diag.ToString()
			};
		}
	}

	private object ResolveTemplateDocument(IComosDProject currentProject, string systemFullName, string description, StringBuilder diag)
	{
		List<string> list = new List<string>();
		string text = (systemFullName ?? "").Trim();
		string text2 = (description ?? "").Trim();
		AddCandidate(list, text);
		AddCandidate(list, text2);
		Match match = Regex.Match(text2, "([@=]?[A-Za-z0-9_\\-|./]{4,})");
		if (match.Success)
		{
			AddCandidate(list, match.Groups[1].Value);
		}
		if (diag != null)
		{
			diag.AppendLine("ResolveTemplateDocument start");
			diag.AppendLine("Candidates=" + string.Join(" | ", list.ToArray()));
		}
		// S1: direct path on current project
		for (int i = 0; i < list.Count; i++)
		{
			object objectByPathFullName = TryGetObjectByPathFullName(currentProject, list[i], diag, "S1");
			if (objectByPathFullName != null)
			{
				return objectByPathFullName;
			}
		}
		// S2: normalized path variants on current project
		List<string> list2 = BuildTemplatePathCandidates(list);
		for (int j = 0; j < list2.Count; j++)
		{
			object objectByPathFullName2 = TryGetObjectByPathFullName(currentProject, list2[j], diag, "S2");
			if (objectByPathFullName2 != null)
			{
				return objectByPathFullName2;
			}
		}
		// S3: same variants prefixed with common template roots
		string[] array = new string[4] { "@Template", "@Templates", "Template", "Templates" };
		for (int k = 0; k < array.Length; k++)
		{
			for (int l = 0; l < list2.Count; l++)
			{
				string text3 = list2[l];
				if (!text3.StartsWith(array[k], StringComparison.OrdinalIgnoreCase))
				{
					text3 = array[k] + "|" + text3.TrimStart('|');
				}
				object objectByPathFullName3 = TryGetObjectByPathFullName(currentProject, text3, diag, "S3");
				if (objectByPathFullName3 != null)
				{
					return objectByPathFullName3;
				}
			}
		}
		// S4: search by path across available projects
		List<object> list3 = EnumerateProjects(currentProject);
		for (int m = 0; m < list3.Count; m++)
		{
			for (int n = 0; n < list2.Count; n++)
			{
				object objectByPathFullName4 = TryGetObjectByPathFullName(list3[m], list2[n], diag, "S4");
				if (objectByPathFullName4 != null)
				{
					return objectByPathFullName4;
				}
			}
		}
		// S5: scan documents by Name/Label/SystemFullName contains candidate text
		for (int num = 0; num < list3.Count; num++)
		{
			object obj = TryFindDocumentByNameScan(list3[num], list, diag, "S5");
			if (obj is IComosDDocument)
			{
				return obj;
			}
		}
		// S6: fallback search manager by Name/Label
		for (int num2 = 0; num2 < list.Count; num2++)
		{
			object obj2 = SearchDocumentByAttribute(29, "Name", list[num2]);
