			{
				try
				{
					int[] deviceTypes = new[] { 8, 1, 29, 2, 3, 4, 5, 6, 7, 9, 10 };
					var candidates = ImportAgent.BuildTagCandidates(objectName);
					string usedCand = ""; int usedType = 0;
					foreach (string cand in candidates)
					{
						if ((object)device != null) break;
						foreach (int dt in deviceTypes)
						{
							try
							{
								object loaded = Workset.LoadObjectByType(dt, cand);
								if (loaded != null) { device = (dynamic)loaded; usedCand = cand; usedType = dt; break; }
							}
							catch { }
						}
					}
					stratDiag += (object)device != null
						? $" S3:LoadByName=FOUND(type={usedType},cand='{usedCand}')"
						: $" S3:LoadByName=NOMATCH(cands=[{string.Join(",", candidates)}])";
				}
				catch (Exception ex) { stratDiag += $" S3:LoadByName=ERR({ex.Message})"; }
			}

			// ── Strategy 4 (LAST RESORT): AllDevices scan ──
			if ((object)device == null && !string.IsNullOrWhiteSpace(objectName))
			{
				IComosDProject currentProject = null;
				try { currentProject = Workset.GetCurrentProject(); } catch { }
				if (currentProject != null)
				{
					try
					{
						dynamic allDevs = ((dynamic)currentProject).AllDevices();
						if (allDevs != null)
						{
							int count = allDevs.Count;
							int maxScan = (count < 10000) ? count : 10000;
							for (int i = 1; i <= maxScan; i++)
							{
								try
								{
									dynamic dev = allDevs.Item(i);
									if (dev != null && ImportAgent.DeviceMatchesTag(dev, objectName))
									{ device = dev; break; }
								}
								catch { }
							}
							stratDiag += (object)device != null
								? $" S4:AllDevices=FOUND"
								: $" S4:AllDevices(count={count})=NOMATCH";
						}
					}
					catch (Exception ex) { stratDiag += $" S4:AllDevices=ERR({ex.Message})"; }
				}
			}

			if ((object)device == null)
			{
				string tried = "";
				if (!string.IsNullOrWhiteSpace(objectName)) tried += $" objectName='{objectName}'";
				if (!string.IsNullOrWhiteSpace(systemUID)) tried += $" systemUID='{systemUID}'";
				if (parsedType > 0) tried += $" systemType={parsedType}";
				return new
				{
					success = false,
					error = $"Could not find the object.{tried} [DIAG: {stratDiag}]"
				};
			}

			string objName = "";
			string objLabel = "";
			string objUID = "";
			try { objName = (string)device.Name; } catch { }
			try { objLabel = (string)device.Label; } catch { }
			try { objUID = (string)device.SystemUID; } catch { }

			// Enumerate specifications recursively (same depth as set_attribute_value)
			var results = new List<string>();
			var diagLog = new List<string>();
			int totalAttrs = 0;
			const int MAX_ATTRS = 500;
			const int MAX_DEPTH = 10;

			try
			{
				dynamic specs = device.Specifications;
				if (specs == null)
				{
					return new
					{
						success = true,
						objectName = objName,
						objectLabel = objLabel,
						systemUID = objUID,
						message = "Object has no specifications.",
						attributes = ""
					};
				}

				int topCount = 0;
				try { topCount = (int)specs.Count; } catch { }
				diagLog.Add($"TopSpecs.Count={topCount}");

				// Tab filter: if provided, only recurse into top-level tabs whose Name or Description matches
				string tabFilterLower = string.IsNullOrWhiteSpace(tabFilter) ? "" : tabFilter.Trim().ToLowerInvariant();

				if (string.IsNullOrEmpty(tabFilterLower))
				{
					// No filter — collect all tabs
					CollectSpecsRecursive(specs, "", 0, MAX_DEPTH, results, ref totalAttrs, MAX_ATTRS, diagLog);
				}
				else
				{
					// Filter: iterate top-level tabs manually and only recurse into matching ones
					int matchedTabs = 0;
					var matchedTabNames = new List<string>();
					for (int ti = 1; ti <= topCount && totalAttrs < MAX_ATTRS; ti++)
					{
						try
						{
							dynamic tabSpec = specs.Item(ti);
							if ((object)tabSpec == null) continue;
							string tName = "", tDesc = "";
							try { tName = (string)tabSpec.Name; } catch { }
							try { tDesc = (string)tabSpec.Description; } catch { }
							string tNameL = (tName ?? "").ToLowerInvariant();
							string tDescL = (tDesc ?? "").ToLowerInvariant();

							// Match: tab name or description contains the filter, or filter contains the tab name/description
							bool tabMatches = tNameL.Contains(tabFilterLower) || tDescL.Contains(tabFilterLower)
								|| tabFilterLower.Contains(tDescL) || tabFilterLower.Contains(tNameL);
							// Also try word overlap: split filter into words and check if all appear in desc
							if (!tabMatches)
							{
								string[] filterWords = tabFilterLower.Split(new[] { ' ', '_', '-' }, StringSplitOptions.RemoveEmptyEntries);
								if (filterWords.Length > 0)
								{
									int hits = 0;
									foreach (string fw in filterWords)
									{
										if (fw.Length < 2) continue;
										if (tDescL.Contains(fw) || tNameL.Contains(fw)) hits++;
									}
									tabMatches = hits == filterWords.Length;
								}
							}

							if (tabMatches)
							{
								matchedTabs++;
								string tabLabel = string.IsNullOrWhiteSpace(tDesc) ? tName : tDesc;
								matchedTabNames.Add(tabLabel);
								// Recurse into this tab's children
								bool tabRecursed = false;
								try {
									dynamic childSpecs = tabSpec.Specs;
									if ((object)childSpecs != null) {
										int cc = 0; try { cc = (int)childSpecs.Count; } catch { }
										if (cc > 0) { tabRecursed = true; CollectSpecsRecursive(childSpecs, tabLabel, 1, MAX_DEPTH, results, ref totalAttrs, MAX_ATTRS, diagLog); }
									}
								} catch { }
								if (!tabRecursed) {
									try {
										dynamic childSpecs2 = tabSpec.Specifications;
										if ((object)childSpecs2 != null) {
											int cc2 = 0; try { cc2 = (int)childSpecs2.Count; } catch { }
											if (cc2 > 0) { tabRecursed = true; CollectSpecsRecursive(childSpecs2, tabLabel, 1, MAX_DEPTH, results, ref totalAttrs, MAX_ATTRS, diagLog); }
										}
									} catch { }
								}
							}
						}
						catch { }
					}
					diagLog.Add($"TabFilter='{tabFilter}' matched={matchedTabs} tabs=[{string.Join(",", matchedTabNames)}]");
				}
			}
			catch (Exception ex)
			{
				return new
				{
					success = false,
					error = $"Error enumerating specifications: {ex.Message}",
					objectName = objName,
					objectLabel = objLabel
				};
			}

			string allAttributes = string.Join("; ", results);
			string diagnostics = string.Join("; ", diagLog);
			return new
			{
				success = true,
				objectName = objName,
				objectLabel = objLabel,
				systemUID = objUID,
				attributeCount = totalAttrs,
				filledOnly = true,
				tabFilter = string.IsNullOrWhiteSpace(tabFilter) ? "(all)" : tabFilter,
				truncated = totalAttrs >= MAX_ATTRS,
				diagnostics = diagnostics,
				attributes = allAttributes
			};
		}
		catch (Exception ex)
		{
			return new
			{
				success = false,
				error = $"Unexpected error: {ex.Message}"
			};
		}
	}

	/// <summary>
	/// Recursively collects ALL spec attributes with Name, Description, Value, SpecValue and SpecUnit.
	/// Uses the same traversal pattern as SearchSpecsRecursive (tries .Specs then .Specifications).
	/// </summary>
	private static void CollectSpecsRecursive(
		dynamic specs, string parentLabel, int depth, int maxDepth,
		List<string> results, ref int totalAttrs, int maxAttrs,
		List<string> diagLog = null)
	{
		if (depth > maxDepth || totalAttrs >= maxAttrs) return;
		if ((object)specs == null) return;

		int count = 0;
		try { count = (int)specs.Count; } catch { return; }

		for (int i = 1; i <= count && totalAttrs < maxAttrs; i++)
		{
			try
			{
				dynamic spec = specs.Item(i);
				if ((object)spec == null) continue;

				string sName = "";
				string sDesc = "";
				try { sName = (string)spec.Name; } catch { }
				try { sDesc = (string)spec.Description; } catch { }
				string sLabel = string.IsNullOrWhiteSpace(sDesc) ? sName : sDesc;
				string fullLabel = string.IsNullOrWhiteSpace(parentLabel) ? sLabel : parentLabel + " > " + sLabel;

				bool isFolder = false;
				try { isFolder = (bool)spec.IsFolder; } catch { }

				// Diagnostic: log every node visited (first 60)
				if (diagLog != null && diagLog.Count < 60)
				{
					string dvDiag = "", svDiag = "", valDiag = "";
					try { dvDiag = (string)spec.DisplayValue ?? ""; } catch (Exception ex) { dvDiag = $"ERR:{ex.GetType().Name}"; }
					try { valDiag = Convert.ToString(((dynamic)spec).value) ?? ""; } catch (Exception ex) { valDiag = $"ERR:{ex.GetType().Name}"; }
					try { svDiag = Convert.ToString(((dynamic)spec).SpecValue) ?? ""; } catch (Exception ex) { svDiag = $"ERR:{ex.GetType().Name}"; }
					diagLog.Add($"d{depth}:{sName}({sDesc})[fld={isFolder}] DV='{dvDiag}' val='{valDiag}' SV='{svDiag}'");
				}

				// Collect this node as an attribute if it has a value (regardless of IsFolder)
				// NOTE: Do NOT use 'continue' here — must always fall through to recursion below.
				if (!isFolder)
				{
					string displayValue = "";
					string specValue = "";
					string specUnit = "";
					try { displayValue = (string)spec.DisplayValue; } catch { }
					if (string.IsNullOrWhiteSpace(displayValue))
					{
						try { displayValue = (string)spec.value; } catch { }
					}
					try { specValue = Convert.ToString(((dynamic)spec).SpecValue); } catch { }
					try { specUnit = (string)spec.Unit; } catch { }
					if (string.IsNullOrWhiteSpace(specUnit))
					{
						try { specUnit = (string)((dynamic)spec).SpecUnit; } catch { }
					}

					// Only include attributes that have at least one filled value
					bool hasAnyValue = !string.IsNullOrWhiteSpace(displayValue)
						|| !string.IsNullOrWhiteSpace(specValue);

					if (hasAnyValue)
					{
						// Build attribute entry: [Tab > SubTab] Name (Description) = Value | SV=specValue U=unit
						string entry = $"[{(string.IsNullOrWhiteSpace(parentLabel) ? "(root)" : parentLabel)}] ";
						entry += string.IsNullOrWhiteSpace(sDesc) ? sName : $"{sName} ({sDesc})";
						if (!string.IsNullOrWhiteSpace(displayValue))
							entry += $" = {displayValue}";
						// Append SpecValue if different from DisplayValue
						if (!string.IsNullOrWhiteSpace(specValue) && specValue != displayValue)
							entry += $" | SV={specValue}";
						if (!string.IsNullOrWhiteSpace(specUnit))
							entry += $" | U={specUnit}";

						results.Add(entry);
						totalAttrs++;
					}
				}

				// ALWAYS recurse into children — same as SearchSpecsRecursive:
				// some COMOS spec nodes have sub-specs even when IsFolder is false.
				bool recursed = false;
				int childCntA = -1, childCntB = -1, childCntC = -1;
				string childErrA = "", childErrB = "", childErrC = "";
				try
				{
					dynamic childSpecs = spec.Specs;
					if ((object)childSpecs != null)
					{
						try { childCntA = (int)childSpecs.Count; } catch (Exception cex) { childErrA = cex.GetType().Name; }
						if (childCntA > 0)
						{
							recursed = true;
							CollectSpecsRecursive(childSpecs, fullLabel, depth + 1, maxDepth,
								results, ref totalAttrs, maxAttrs, diagLog);
						}
					}
					else { childErrA = "null"; }
				}
				catch (Exception ex) { childErrA = ex.GetType().Name; }
				// Fallback: try .Specifications
				if (!recursed)
				{
					try
					{
						dynamic childSpecs2 = spec.Specifications;
						if ((object)childSpecs2 != null)
						{
							try { childCntB = (int)childSpecs2.Count; } catch (Exception cex) { childErrB = cex.GetType().Name; }
							if (childCntB > 0)
							{
								recursed = true;
								CollectSpecsRecursive(childSpecs2, fullLabel, depth + 1, maxDepth,
									results, ref totalAttrs, maxAttrs, diagLog);
							}
						}
						else { childErrB = "null"; }
					}
					catch (Exception ex) { childErrB = ex.GetType().Name; }
				}
				// Fallback 2: try device-style .Specifications on the spec node (COMOS tabs)
				if (!recursed)
				{
					try
					{
						object rawObj = (object)spec;
						// Try to call Specifications property via COM interop reflection
						var specType = rawObj.GetType();
						var specsProp = specType.GetProperty("Specifications");
						if (specsProp == null)
						{
							// Try via InvokeMember for late-bound COM
							object childObj = specType.InvokeMember("Specifications",
								System.Reflection.BindingFlags.GetProperty | System.Reflection.BindingFlags.Instance | System.Reflection.BindingFlags.Public,
								null, rawObj, null);
							if (childObj != null)
							{
								dynamic childDyn = childObj;
								try { childCntC = (int)childDyn.Count; } catch (Exception cex) { childErrC = cex.GetType().Name; }
								if (childCntC > 0)
								{
									recursed = true;
									CollectSpecsRecursive(childDyn, fullLabel, depth + 1, maxDepth,
										results, ref totalAttrs, maxAttrs, diagLog);
								}
							}
							else { childErrC = "InvokeMember=null"; }
						}
					}
					catch (Exception ex) { childErrC = ex.GetType().Name; }
				}
				// Diagnostic: log child accessor results for depth 0
				if (diagLog != null && depth == 0 && diagLog.Count < 80)
				{
					diagLog.Add($"  children({sName}): Specs={childCntA}({childErrA}) Specifications={childCntB}({childErrB}) Reflect={childCntC}({childErrC}) recursed={recursed}");
				}
			}
			catch { }
		}
	}

	// ══════════════════════════════════════════════════════════════════════
	// SET / WRITE ATTRIBUTE VALUE
	// ══════════════════════════════════════════════════════════════════════
	[AiFunction("set_attribute_value",
		"Sets or overwrites the value of a specification attribute on a COMOS object. " +
		"Finds the attribute by name or description (fuzzy match). " +
		"Use when the user asks to set, change, update, edit, or write an attribute value. " +
		"Returns the old value and the new value after the write.")]
	public object SetAttributeValue(
		[DescribeParameter("Name or description of the attribute to set (e.g. 'shaft power', 'potência', 'modelo'). Fuzzy matching is used.", ExampleValue = "shaft power")]
		string attributeName,
		[DescribeParameter("The new value to write to the attribute.", ExampleValue = "75")]
		string newValue,
		[DescribeParameter("Optional: SystemUID of the COMOS object.", ExampleValue = "")]
		string systemUID,
		[DescribeParameter("Optional: Name/tag of the COMOS object (e.g. M001, PC001). Preferred way to identify the object.", ExampleValue = "")]
		string objectName,
		[DescribeParameter("Optional: SystemType returned by navigate (e.g. 8). Enables direct LoadObjectByType(type, uid).", ExampleValue = "")]
		string systemType)
	{
		if (Workset == null)
		{
			return new
			{
				success = false,
				error = "Workset not available. COMOS must be running with an open project."
			};
		}

		if (string.IsNullOrWhiteSpace(attributeName))
		{
			return new
			{
				success = false,
				error = "attributeName is required. Provide the name or description of the attribute to set."
			};
		}

		try
		{
			dynamic device = null;
			string stratDiag = "";

			// ── Strategy 1 (GOLDEN PATH): LoadObjectByType(systemType, systemUID) ──
			// The shim ALWAYS navigates first and passes systemUID + systemType.
			int parsedType = 0;
			if (!string.IsNullOrWhiteSpace(systemType)) int.TryParse(systemType, out parsedType);

			if ((object)device == null && !string.IsNullOrWhiteSpace(systemUID) && parsedType > 0)
			{
				try
				{
					object loaded = Workset.LoadObjectByType(parsedType, systemUID);
					if (loaded != null)
					{
						device = (dynamic)loaded;
						stratDiag += $"S1:LoadByType({parsedType},'{systemUID}')=FOUND";
					}
					else { stratDiag += $"S1:LoadByType({parsedType},'{systemUID}')=null"; }
				}
				catch (Exception ex) { stratDiag += $"S1:LoadByType({parsedType},'{systemUID}')=ERR({ex.Message})"; }
			}
			else
			{
				stratDiag += $"S1:SKIP(uid={!string.IsNullOrWhiteSpace(systemUID)},type={parsedType})";
			}

			// ── Strategy 2: LoadObjectByType(candidateTypes, systemUID) ──
			if ((object)device == null && !string.IsNullOrWhiteSpace(systemUID))
			{
				try
				{
					int[] fallbackTypes = new[] { 8, 1, 29, 2, 3, 4, 5, 6, 7, 9, 10 };
					int usedType = 0;
					foreach (int dt in fallbackTypes)
					{
						if (dt == parsedType) continue;
						try
						{
							object loaded = Workset.LoadObjectByType(dt, systemUID);
							if (loaded != null) { device = (dynamic)loaded; usedType = dt; break; }
						}
						catch { }
					}
					stratDiag += (object)device != null
						? $" S2:LoadByType(scan,'{systemUID}')=FOUND(type={usedType})"
						: $" S2:LoadByType(scan,'{systemUID}')=NOMATCH";
				}
				catch (Exception ex) { stratDiag += $" S2:LoadByType(scan)=ERR({ex.Message})"; }
			}

			// ── Strategy 3: LoadObjectByType by objectName candidates ──
			if ((object)device == null && !string.IsNullOrWhiteSpace(objectName))
			{
				try
				{
					int[] deviceTypes = new[] { 8, 1, 29, 2, 3, 4, 5, 6, 7, 9, 10 };
					var candidates = ImportAgent.BuildTagCandidates(objectName);
					foreach (string cand in candidates)
					{
						if ((object)device != null) break;
						foreach (int dt in deviceTypes)
						{
							try
							{
								object loaded = Workset.LoadObjectByType(dt, cand);
								if (loaded != null) { device = (dynamic)loaded; break; }
							}
							catch { }
						}
					}
					stratDiag += (object)device != null
						? $" S3:LoadByName=FOUND"
						: $" S3:LoadByName=NOMATCH";
				}
				catch (Exception ex) { stratDiag += $" S3:LoadByName=ERR({ex.Message})"; }
			}

			// ── Strategy 4 (LAST RESORT): AllDevices scan ──
			if ((object)device == null && !string.IsNullOrWhiteSpace(objectName))
			{
				IComosDProject currentProject = null;
				try { currentProject = Workset.GetCurrentProject(); } catch { }
				if (currentProject != null)
				{
					try
					{
						dynamic allDevs = ((dynamic)currentProject).AllDevices();
						if (allDevs != null)
						{
							int count = allDevs.Count;
							int maxScan = (count < 10000) ? count : 10000;
							for (int i = 1; i <= maxScan; i++)
							{
								try
								{
									dynamic dev = allDevs.Item(i);
									if (dev != null && ImportAgent.DeviceMatchesTag(dev, objectName))
									{ device = dev; break; }
								}
								catch { }
							}
							stratDiag += (object)device != null
								? $" S4:AllDevices=FOUND"
								: $" S4:AllDevices(count={count})=NOMATCH";
						}
					}
					catch (Exception ex) { stratDiag += $" S4:AllDevices=ERR({ex.Message})"; }
				}
			}

			if ((object)device == null)
			{
				string tried = "";
				if (!string.IsNullOrWhiteSpace(objectName)) tried += $" objectName='{objectName}'";
				if (!string.IsNullOrWhiteSpace(systemUID)) tried += $" systemUID='{systemUID}'";
				if (parsedType > 0) tried += $" systemType={parsedType}";
				return new
				{
					success = false,
					error = $"Could not find the object.{tried} [DIAG: {stratDiag}]"
				};
			}

			string objName = "";
			string objLabel = "";
			string objUID = "";
			try { objName = (string)device.Name; } catch { }
			try { objLabel = (string)device.Label; } catch { }
			try { objUID = (string)device.SystemUID; } catch { }

			// ── Find the matching attribute by Name or Description (fuzzy) ──
			string searchLower = attributeName.Trim().ToLowerInvariant();
			dynamic bestMatch = null;
			string bestName = "";
			string bestDesc = "";
			string bestOldValue = "";
			string bestTabLabel = "";
			int bestScore = int.MaxValue; // lower is better (Levenshtein distance)
			var specDump = new System.Collections.Generic.List<string>();

			try
			{
				dynamic specs = device.Specifications;
				if (specs == null)
				{
					return new
					{
						success = false,
						error = "Object has no specifications.",
						objectName = objName
					};
				}

				// Recursive search through specs (up to 10 levels, matching native DLL behavior)
				SearchSpecsRecursive(specs, searchLower, "", 0, 10,
					ref bestMatch, ref bestName, ref bestDesc, ref bestOldValue, ref bestTabLabel, ref bestScore,
					specDump);
			}
			catch (Exception ex)
			{
				return new
				{
					success = false,
					error = $"Error searching specifications: {ex.Message}",
					objectName = objName
				};
			}

			// Threshold: Levenshtein distance > 5 means no reasonable match
			if ((object)bestMatch == null || bestScore > 5)
			{
				string specTree = string.Join("; ", specDump.Count > 40 ? specDump.GetRange(0, 40) : specDump);
				return new
				{
					success = false,
					error = $"Attribute '{attributeName}' not found on object '{objName}'. Use list_object_attributes to see available attributes.",
					objectName = objName,
					systemUID = objUID,
					closestMatch = (object)bestMatch != null ? $"{bestName} ({bestDesc}) [score={bestScore}]" : "none",
					specsFound = specTree
				};
			}

			// ── Write the new value ──
			string writeError = "";
			bool written = false;
			try
			{
				bestMatch.Value = newValue;
				written = true;
			}
			catch (Exception ex1)
			{
				// Fallback: try DisplayValue setter
				try
				{
					bestMatch.DisplayValue = newValue;
					written = true;
				}
				catch (Exception ex2)
				{
					writeError = $"Value setter failed: {ex1.Message}; DisplayValue setter failed: {ex2.Message}";
				}
			}

			if (!written)
			{
				return new
				{
					success = false,
					error = writeError,
					objectName = objName,
					attributeName = $"{bestName} ({bestDesc})",
					tab = bestTabLabel,
					oldValue = bestOldValue
				};
			}

			// Read back the value to confirm
			string confirmedValue = "";
			try { confirmedValue = (string)bestMatch.DisplayValue; } catch { }
			if (string.IsNullOrWhiteSpace(confirmedValue))
			{
				try { confirmedValue = (string)bestMatch.value; } catch { }
			}

			return new
			{
				success = true,
				objectName = objName,
				objectLabel = objLabel,
				systemUID = objUID,
				attributeName = $"{bestName} ({bestDesc})",
				tab = bestTabLabel,
				oldValue = bestOldValue,
				newValue = confirmedValue,
				matchScore = bestScore
			};
		}
		catch (Exception ex)
		{
			return new
			{
				success = false,
				error = $"Unexpected error: {ex.Message}"
			};
		}
	}

	/// <summary>
	/// Recursively searches through COMOS Specifications to find the best-matching attribute.
	/// Mirrors the native DLL's search_spec_recursive behavior (up to maxDepth levels).
	/// </summary>
	private static void SearchSpecsRecursive(
		dynamic specs, string searchLower, string parentLabel, int depth, int maxDepth,
		ref dynamic bestMatch, ref string bestName, ref string bestDesc,
		ref string bestOldValue, ref string bestTabLabel, ref int bestScore,
		System.Collections.Generic.List<string> specDump = null)
	{
		if (depth > maxDepth || bestScore == 0) return;
		if ((object)specs == null) return;

		int count = 0;
		try { count = (int)specs.Count; } catch { return; }

		for (int i = 1; i <= count; i++)
		{
			if (bestScore == 0) break; // exact match already found

			try
			{
				dynamic spec = specs.Item(i);
				if ((object)spec == null) continue;

				string sName = "";
				string sDesc = "";
				try { sName = (string)spec.Name; } catch { }
				try { sDesc = (string)spec.Description; } catch { }
				string sLabel = string.IsNullOrWhiteSpace(sDesc) ? sName : sDesc;
				string fullLabel = string.IsNullOrWhiteSpace(parentLabel) ? sLabel : parentLabel + " > " + sLabel;

				// Diagnostic dump
				if (specDump != null && specDump.Count < 80)
					specDump.Add($"d{depth}:{(string.IsNullOrWhiteSpace(sDesc) ? sName : sName + "(" + sDesc + ")")}");

				// Score this node as a potential attribute match (regardless of folder status)
				bool isFolder = false;
				try { isFolder = (bool)spec.IsFolder; } catch { }

				if (!isFolder)
				{
					int score = ScoreAttributeMatch(searchLower, sName, sDesc);
					if (score < bestScore)
					{
						bestScore = score;
						bestMatch = spec;
						bestName = sName;
						bestDesc = sDesc;
						bestTabLabel = string.IsNullOrWhiteSpace(parentLabel) ? "(top-level)" : parentLabel;
						try { bestOldValue = (string)spec.DisplayValue; } catch { }
						if (string.IsNullOrWhiteSpace(bestOldValue))
						{
							try { bestOldValue = (string)spec.value; } catch { }
						}
					}
				}

				// ALWAYS try to recurse into children — some COMOS spec nodes
				// have sub-specs (.Specs or .Specifications) even when IsFolder is false.
				bool recursed = false;
				// Try .Specs first (standard child accessor)
				try
				{
					dynamic childSpecs = spec.Specs;
					if ((object)childSpecs != null)
					{
						int childCount = 0;
						try { childCount = (int)childSpecs.Count; } catch { }
						if (childCount > 0)
						{
							recursed = true;
							SearchSpecsRecursive(childSpecs, searchLower, fullLabel, depth + 1, maxDepth,
								ref bestMatch, ref bestName, ref bestDesc,
								ref bestOldValue, ref bestTabLabel, ref bestScore,
								specDump);
						}
					}
				}
				catch { }
				// Fallback: try .Specifications (some COMOS COM objects use this)
				if (!recursed)
				{
					try
					{
						dynamic childSpecs2 = spec.Specifications;
						if ((object)childSpecs2 != null)
						{
							int childCount2 = 0;
							try { childCount2 = (int)childSpecs2.Count; } catch { }
							if (childCount2 > 0)
							{
								SearchSpecsRecursive(childSpecs2, searchLower, fullLabel, depth + 1, maxDepth,
									ref bestMatch, ref bestName, ref bestDesc,
									ref bestOldValue, ref bestTabLabel, ref bestScore,
									specDump);
							}
						}
					}
					catch { }
				}
			}
			catch { }
		}
	}

	/// <summary>
	/// Scores how well a search term matches an attribute name/description.
	/// Returns 0 for exact match, higher values for worse matches.
	/// </summary>
	private static int ScoreAttributeMatch(string searchLower, string name, string description)
	{
		string nameLower = (name ?? "").Trim().ToLowerInvariant();
		string descLower = (description ?? "").Trim().ToLowerInvariant();

		// Exact match on name or description
		if (nameLower == searchLower || descLower == searchLower) return 0;

		// Contains match
		if (nameLower.Contains(searchLower) || descLower.Contains(searchLower)) return 1;
		if (searchLower.Contains(nameLower) && nameLower.Length > 2) return 1;
		if (searchLower.Contains(descLower) && descLower.Length > 2) return 1;

		// Word overlap scoring
		string[] searchWords = searchLower.Split(new[] { ' ', '_', '-', '.' }, StringSplitOptions.RemoveEmptyEntries);
		int wordHits = 0;
		foreach (string w in searchWords)
		{
			if (w.Length < 2) continue;
			if (nameLower.Contains(w) || descLower.Contains(w)) wordHits++;
		}
		if (wordHits > 0 && searchWords.Length > 0)
		{
			// 2 = all words match, 3 = most words match, etc.
			return 2 + (searchWords.Length - wordHits);
		}

		// Levenshtein on description (most human-readable)
		if (descLower.Length > 0)
		{
			int dist = LevenshteinDistance(searchLower, descLower);
			// Normalize: if description is much longer than search, substring match is better
			if (descLower.Length > searchLower.Length * 2)
			{
				// Try sliding window
				int minDist = dist;
				for (int start = 0; start <= descLower.Length - searchLower.Length; start++)
				{
					int d = LevenshteinDistance(searchLower, descLower.Substring(start, searchLower.Length));
					if (d < minDist) minDist = d;
				}
				return 3 + minDist;
			}
			return 3 + dist;
		}

		// Levenshtein on name
		if (nameLower.Length > 0)
		{
			return 3 + LevenshteinDistance(searchLower, nameLower);
		}

		return int.MaxValue;
	}

	/// <summary>
	/// Standard Levenshtein distance between two strings.
	/// </summary>
	private static int LevenshteinDistance(string a, string b)
	{
		if (string.IsNullOrEmpty(a)) return (b ?? "").Length;
		if (string.IsNullOrEmpty(b)) return a.Length;

		int[,] dp = new int[a.Length + 1, b.Length + 1];
		for (int i = 0; i <= a.Length; i++) dp[i, 0] = i;
		for (int j = 0; j <= b.Length; j++) dp[0, j] = j;

		for (int i = 1; i <= a.Length; i++)
		{
			for (int j = 1; j <= b.Length; j++)
			{
				int cost = (a[i - 1] == b[j - 1]) ? 0 : 1;
				dp[i, j] = Math.Min(
					Math.Min(dp[i - 1, j] + 1, dp[i, j - 1] + 1),
					dp[i - 1, j - 1] + cost);
			}
		}

		return dp[a.Length, b.Length];
	}
